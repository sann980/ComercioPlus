    //Metodo para listar na tabela
    public void listar() {
        ClienteDao dao = new ClienteDao();
        List<Clientes> list = dao.listarClientes();
        DefaultTableModel dados = (DefaultTableModel) TabelaClientes.getModel();

        dados.setNumRows(0); //Apaga tudo que tem na tabela

        for (Clientes c : list) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getRg(),
                c.getCpf(),
                c.getEmail(),
                c.getTelefone(),
                c.getCelular(),
                c.getCep(),
                c.getEndereco(),
                c.getNumero(),
                c.getCmplemento(),
                c.getBairro(),
                c.getCidade(),
                c.getEstado()
            });
        }
    }

