    private void tabelaItensMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaItensMouseClicked
        // Evento ao clicar em um produto do carrinho
            FrmDesconto telaDesconto = new FrmDesconto();
            telaDesconto.telaVendas = this;
            
            telaDesconto.subtotal = Double.parseDouble(tabelaItens.getValueAt(tabelaItens.getSelectedRow(), 5).toString().substring(3, 
                                    tabelaItens.getValueAt(tabelaItens.getSelectedRow(), 5).toString().length()
                                    ).replaceAll(",", ".")); //Passar sutotal para a tela desconto
            
            telaDesconto.linha = tabelaItens.getSelectedRow();
            
            telaDesconto.setVisible(true);
    }//GEN-LAST:event_tabelaItensMouseClicked

