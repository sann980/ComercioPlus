    private void btnAdicionarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarItemActionPerformed
            // Busca produto por código de barras
            Produtos obj = new Produtos();
            ProdutosDao dao = new ProdutosDao();
            
            obj = dao.buscarProdutoPorCodBarras(txtCodigo.getText());
            
            if(obj.getCodBarras() != null){
                //System.out.println(txtQtdProduto.getText());
            
                qtd = Integer.parseInt(txtQtdProduto.getText());
                preco = obj.getPrecoVarejo();
        
                subtotal = qtd * preco;
        
                totalComDesconto += subtotal;
                txtTotalGeral.setText(String.valueOf(NumberFormat.getCurrencyInstance().format(totalComDesconto)));
                txtTotalDesconto.setText(String.valueOf(NumberFormat.getCurrencyInstance().format(desconto)));
                
        
                //Adicionando o produto ao carrinho
                carrinho = (DefaultTableModel) tabelaItens.getModel();
        
                carrinho.addRow(new Object[]{
                    obj.getCodBarras(),
                    obj.getDescricao(),
                    NumberFormat.getCurrencyInstance().format(obj.getPrecoVarejo()),
                    txtQtdProduto.getText(),
                    null, //Desconto
                    NumberFormat.getCurrencyInstance().format(subtotal)
                });
                txtCodigo.setText("");
            }
            else{
                JOptionPane.showMessageDialog(null, "Produto não encontrado na base de dados");
                txtCodigo.setText("");
            }
        
        //Buscar por ID
        /*Produtos obj = new Produtos();
            ProdutosDao dao = new ProdutosDao();
            
            obj = dao.buscarProdutoPorId(Integer.parseInt(txtCodigo.getText()));
            
            txtDescricaoProduto.setText(obj.getDescricao());
            txtPreco.setText(String.valueOf(obj.getPreco()));
            
            txtQtdProduto.setText("1");*/

    }//GEN-LAST:event_btnAdicionarItemActionPerformed

