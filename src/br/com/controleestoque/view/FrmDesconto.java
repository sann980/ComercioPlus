    private void concederDesconto() {
        telaVendas.tabelaItens.setValueAt(NumberFormat.getCurrencyInstance().format(valorDesconto), linha, 4);
        telaVendas.tabelaItens.setValueAt(NumberFormat.getCurrencyInstance().format(subtotalAtulizado), linha, 5);
        telaVendas.totalComDesconto -= valorDesconto;
        telaVendas.desconto += valorDesconto;
        telaVendas.txtTotalGeral.setText(String.valueOf(NumberFormat.getCurrencyInstance().format(telaVendas.totalComDesconto)));
        telaVendas.txtTotalDesconto.setText(String.valueOf(NumberFormat.getCurrencyInstance().format(telaVendas.desconto)));
    }

    private void cancelarVenda() {
        DefaultTableModel model = (DefaultTableModel) telaVendas.tabelaItens.getModel();
        model.removeRow(linha);

        telaVendas.totalComDesconto -= subtotal;
        telaVendas.desconto -= valorDesconto;
        telaVendas.txtTotalGeral.setText(String.valueOf(NumberFormat.getCurrencyInstance().format(telaVendas.totalComDesconto)));
        telaVendas.txtTotalDesconto.setText(String.valueOf(NumberFormat.getCurrencyInstance().format(telaVendas.desconto)));
        telaVendas.tabelaItens.updateUI();
    }
