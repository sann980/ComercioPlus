    private void btnCederDescontoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCederDescontoActionPerformed
        // Botão que calcula o totalComDesconto de vendas por data
        if ("123".equals(txtSenhaDesconto.getText())) {

            desconto = Double.parseDouble(txtDesconto.getText());

            if (desconto >= 2) {
                if (desconto <= 20) {
                    descontoConcedido = desconto / 100;

                    valorDesconto = subtotal * descontoConcedido; //Calcula o valor a ser retirado do sbtotal

                    subtotalAtulizado = subtotal - valorDesconto;

                    concederDesconto();

                    JOptionPane.showMessageDialog(null, "Desconto concedido.");

                    this.dispose();

                } else {

                    JOptionPane.showMessageDialog(null, "O desconto informado deve está no intervalo entre 2 à 20%");

                }

            } else {

                JOptionPane.showMessageDialog(null, "O desconto informado deve está no intervalo entre 2 à 20%");

            }

        } else {
            JOptionPane.showMessageDialog(null, "Senha incorreta.");
        }
    }//GEN-LAST:event_btnCederDescontoActionPerformed

    private void btnCederDesconto1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCederDesconto1ActionPerformed
        // Botão cancelar venda
        if ("123".equals(txtSenhaCancelar.getText())) {

            DefaultTableModel modelItens = (DefaultTableModel) telaVendas.tabelaItens.getModel();

            if ((modelItens.getValueAt(linha, 4)) != null) {
                valorDesconto = Double.parseDouble(modelItens.getValueAt(linha, 4).toString().
                        substring(3, modelItens.getValueAt(linha, 4).toString().length()).replaceAll(",", "."));
            } else {
                valorDesconto = 0;
            }

            cancelarVenda();

            JOptionPane.showMessageDialog(null, "Lançamento cancelado.");

            this.dispose();

        } else {
            JOptionPane.showMessageDialog(null, "Senha incorreta.");
        }
    }//GEN-LAST:event_btnCederDesconto1ActionPerformed

